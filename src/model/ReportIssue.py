    def __init__(self):
        self.__photograph: Photograph = None
        self.__location: Location = None
        self.__summary: str = None

    @property
    def __photograph(self):
        return

    @__photograph.setter
    def __photograph(self, value):
        pass

    @__photograph.deleter
    def __photograph(self):
        pass

    @property
    def __location(self):
        return

    @__location.setter
    def __location(self, value):
        pass

    @__location.deleter
    def __location(self):
        pass

    @property
    def __summary(self):
        return

    @__summary.setter
    def __summary(self, value):
        pass

    @__summary.deleter
    def __summary(self):
        pass
