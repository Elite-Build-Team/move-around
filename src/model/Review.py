# -*- coding: UTF-8 -*-
class Review(object):
    def __init__(self):
        self.__description: str = None
        self.__stars: int = None

    @property
    def __description(self):
        return

    @__description.setter
    def __description(self, value):
        pass

    @__description.deleter
    def __description(self):
        pass

    @property
    def __stars(self):
        return

    @__stars.setter
    def __stars(self, value):
        pass

    @__stars.deleter
    def __stars(self):
        pass
