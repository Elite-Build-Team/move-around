# -*- coding: UTF-8 -*-
class User(object):
    def __init__(self):
        self.__name: str = None
        self.__surname: str = None
        self.__email: str = None

    @property
    def __name(self):
        return

    @__name.setter
    def __name(self, value):
        pass

    @__name.deleter
    def __name(self):
        pass

    @property
    def __surname(self):
        return

    @__surname.setter
    def __surname(self, value):
        pass

    @__surname.deleter
    def __surname(self):
        pass

    @property
    def __email(self):
        return

    @__email.setter
    def __email(self, value):
        pass

    @__email.deleter
    def __email(self):
        pass
